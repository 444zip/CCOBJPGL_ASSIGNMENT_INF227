package model;

class Restaurants {

    private String bestseller;
    private String rate;

    public String getBestSeller() {
        return this.bestseller;
    }

    public void setBestSeller(String bestseller) {
        this.bestseller = bestseller;
    }

    public String getRate() {
        return this.rate;
    }

    public void setRate(String rate) {
        this.rate = rate;
    }
}
